// ── Metal Synergy: Burn Order Helper ──
// Returns the value of burning cardA to fuel cards sharing its metal pair.

export function burnSynergyValue(
  burnCard: Action,
  snap: GameStateSnapshot,
): number {
  if (burnCard.metal < 0) return 0;

  const pairIdx = burnCard.metal === 8 ? -1 : Math.floor(burnCard.metal / 2);
  let bestTargetValue = 0;

  for (const card of snap.handActionCards) {
    if (card === burnCard) continue;
    if (card.burned || card.metalUsed >= card.capacity) continue;

    // Note: previously had canFuel check here but was superseded by canFuelCorrect below.
    // See git history if needed.

    // Actually: burning a card generates metal for EITHER metal in its pair.
    // A card uses metalAvailable[card.metal]. So burning card with metal=2 generates
    // metalAvailable for bronze(2) or copper(3). Card with metal=3 would use metalAvailable[3].
    // So we need: the target card's metal to be one of the two metals in the burned card's pair.
    const canFuelCorrect =
      burnCard.metal === 8 || // atium can become any metal
      (card.metal >= 0 && card.metal < 8 && Math.floor(card.metal / 2) === pairIdx);

    if (canFuelCorrect) {
      const nextTier = card.metalUsed + 1;
      const effects: [string, string][] = [
        [card.data[3], card.data[4]],
        [card.data[5], card.data[6]],
        [card.data[7], card.data[8]],
      ];
      if (nextTier >= 1 && nextTier <= 3 && effects[nextTier - 1][0]) {
        const val = estimateEffectValue(effects[nextTier - 1][0], effects[nextTier - 1][1], snap);
        if (val > bestTargetValue) bestTargetValue = val;
      }
    }
  }

  return bestTargetValue;
}

// ── Dynamic Buffer ──

export function dynamicBuffer(character: string, snap: GameStateSnapshot): number {
  // Base buffer: how good a card must be to justify buying (and diluting the deck).
  // Higher = more selective. These are calibrated against analyticalRatings.
  const BASE_BUFFERS: Record<string, number> = {
    Kelsier: 1.5,
    Marsh: 1.8,
    Shan: 1.7,   // raised — Shan has money surplus, should be MORE selective not less
    Vin: 1.5,
    Prodigy: 1.4,
  };

  let buffer = BASE_BUFFERS[character] ?? 1.5;

  if (snap.gamePhase === "early") buffer -= 0.3;
  if (snap.curMoney >= 8) buffer -= 0.2;
  if (snap.gamePhase === "late") buffer += 0.4;
  if (snap.deckSize >= 15) buffer += 0.3;
  if (snap.deckSize >= 18) buffer += 0.5;

  return buffer;
}
